package ec.edu.intsuperior.modelo;

/**
 *
 * @author USUARIO
 */
public class ColorOjos {
    private String cafes;
    private String azules;
    private String verdes;
    private String negros;
    private String ambar;

    public ColorOjos() {
    }

    public ColorOjos(String cafes, String azules, String verdes, String negros, String ambar) {
        this.cafes = cafes;
        this.azules = azules;
        this.verdes = verdes;
        this.negros = negros;
        this.ambar = ambar;
    }

    public String getCafes() {
        return cafes;
    }

    public void setCafes(String cafes) {
        this.cafes = cafes;
    }

    public String getAzules() {
        return azules;
    }

    public void setAzules(String azules) {
        this.azules = azules;
    }

    public String getVerdes() {
        return verdes;
    }

    public void setVerdes(String verdes) {
        this.verdes = verdes;
    }

    public String getNegros() {
        return negros;
    }

    public void setNegros(String negros) {
        this.negros = negros;
    }

    public String getAmbar() {
        return ambar;
    }

    public void setAmbar(String ambar) {
        this.ambar = ambar;
    }
   
}
